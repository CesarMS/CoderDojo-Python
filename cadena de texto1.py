nueva_cadena = ""                           # PASO 1: Inicializo acumulador
frase = "Esta es una frase de prueba"       # PASO 2: la frase

for letra in frase:                         # PASO 3: recorro la frase
    nueva_cadena = nueva_cadena + letra     # PASO 4: acumulo letras

print(nueva_cadena)                         # PASO 5: imprimo el resultado

