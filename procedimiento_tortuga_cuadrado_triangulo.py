def cuadrado(tortuga, lado):
    tortuga.forward(lado)           
    tortuga.right(90)              
    tortuga.forward(lado)           
    tortuga.right(90)              
    tortuga.forward(lado)           
    tortuga.right(90)              
    tortuga.forward(lado)           
    tortuga.right(90)

def triangulo(tortuga, lado):
    tortuga.forward(lado)           
    tortuga.right(120)             
    tortuga.forward(lado)           
    tortuga.right(120)              
    tortuga.forward(lado)

   

