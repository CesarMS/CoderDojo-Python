def cuadrado(tortuga):
    tortuga.forward(100)
    tortuga.right(90)
    tortuga.forward(100)
    tortuga.right(90)
    tortuga.forward(100)
    tortuga.right(90)
    tortuga.forward(100)
    tortuga.right(90)

